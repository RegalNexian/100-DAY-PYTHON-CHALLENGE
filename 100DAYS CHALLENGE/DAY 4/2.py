userInput = input("Enter a list of number separated by space!  \n")
myList = userInput.split()


print("Positive Offset")
print(f"Element at index at 7 is {myList[7]}")
print(f"Element at index at 3 is {myList[3]}")
print(f"Element at index at 1 is {myList[1]}")

print("Negative Offset")
print(f"Element at index at -3 is {myList[-3]}")
print(f"Element at index at -4 is {myList[-4]}")
print(f"Element at index at -5 is {myList[-5]}")




